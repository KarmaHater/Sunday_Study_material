//  It is a system that recognizes hierarchy, importance, type, and role of each and every component.
//  The components are combined in a way that resembles the structure of our universe.

//  atoms (button, labels, inputs and other html elements)
//  molecules (button collection,  forms, input + button) // now have purpose (search)
//  organisms (widget eg navbar with some buttons, logo, and search box)
//  templates (Templates are page-level objects that place components into a layout and
//  articulate the design’s underlying content structure.) (skeleton)
//  pages (Pages are specific instances of templates that show what a UI looks like with real representative content in place)

// In the natural world, atomic elements combine together to form molecules.
// These molecules can combine further to form relatively complex organisms.

